class Solution:
    def arrayStringsAreEqual(self, word1: List[str], word2: List[str]) -> bool:
        word_sum1 = ''
        for word in word1:
            word_sum1 += word

        word_sum2 = ''
        for word in word2:
            word_sum2 += word

        return word_sum1 == word_sum2


